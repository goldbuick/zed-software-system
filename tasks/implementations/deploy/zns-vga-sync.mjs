import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '../../..')
const fontsource = join(root, 'ops/infra/zns-public/fonts/PerfectDOSVGA437.woff2')
const fontdest = join(root, 'ops/infra/generated/zns-vga-font.js')

mkdirSync(dirname(fontdest), { recursive: true })
const fontb64 = readFileSync(fontsource).toString('base64')
writeFileSync(
  fontdest,
  `/** Generated by zns:vga:sync — do not edit. */\nexport const ZNS_VGA_FONT_DATA_URI = 'data:font/woff2;base64,${fontb64}'\n`,
)
console.log('wrote ops/infra/generated/zns-vga-font.js')
